package day11dowhileconstructors.reviewquestions;

public class Q03 {

    public static void main(String[] args) {

        int i=1;

        int sum=0;

        while(i<=5) {

            sum = sum + i; //0+1=1; 1+2=3; 1+2+3+4+5=15

            i++; //i=1 ; i=2;

        }

        System.out.println(sum); //it will print final value which is 15

        //output is 15


    }
}
