public class Q06 {

    public static void main(String[] args) {

        /*
 Print on the console numbers from 1 to 100.
 But do not use any number in your code!
 */

        int n = 'a'/'a';
        //this means 97/97 and it gives 1 so n = 1 we can also use 'b' - 'a' so 98-97=1, n is still equal to 1
        int m = 'd'; // ascii value of d is 100 so m = 100

        while(n<=m){

            System.out.print(n + " ");
            n++;
        }


    }
}
