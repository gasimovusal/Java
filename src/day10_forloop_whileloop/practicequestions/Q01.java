package day10_forloop_whileloop.practicequestions;

public class Q01 {

    public static void main(String[] args) {

        for(int i=1; i<=6; i++){ //i=1, i will be result of i++ ; i=2+1=3; i=4+1=5
            System.out.print(i + " "); // it prints output from first part ==> 1 3 5
            i++; // i=1+1=2; i=3+1=4;
        }

        //output will be 1 3 5
    }
}
